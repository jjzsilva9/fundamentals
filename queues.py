class Queue:
    def __init__(self):
        self.last = 0
        self.first = 0
        self.q = []

    def enQueue(self, val):
        self.q.append(val)
        last += 1

    def deQueue(self):
        self.q.pop()
        first += 1
        
    def isEmpty():
        if last == first:
            return True

    def isFull():
        if self.first == 0:
            return True

        

